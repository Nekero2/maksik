function generateQuotes() {
    const container = document.getElementById('quotes-container');
    container.innerHTML = '';

    // Выбираем 10 случайных цитат
    const shuffledQuotes = [...quotes].sort(() => 0.5 - Math.random());
    const selectedQuotes = shuffledQuotes.slice(0, 10);

    selectedQuotes.forEach(quote => {
        const quoteElement = document.createElement('div');
        quoteElement.className = 'quote';
        quoteElement.innerHTML = `
            <p>${quote}</p>
            <div class="author">— Максим Курчевский</div>
        `;
        container.appendChild(quoteElement);
    });
}

// Генерация при загрузке страницы
document.addEventListener('DOMContentLoaded', generateQuotes);

// Генерация по клику
document.getElementById('generate-btn').addEventListener('click', generateQuotes);
